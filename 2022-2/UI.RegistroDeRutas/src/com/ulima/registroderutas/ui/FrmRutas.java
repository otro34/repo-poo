    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int index = this.grdRutas.getSelectedRow(); //Obtiene el índice de la fila seleccionada en la grilla
        
        if (index >= 0) {
            int codigo = Integer.parseInt(this.rutasDtModel.getValueAt(index,0).toString()); //Obtenemos el codigo de la ruta desde la fila en la grilla
            
            Ruta ruta = this.objGestionRutas.buscar(codigo);
            
            if (ruta != null) {
                this.objGestionRutas.remover(ruta);
                
                this.cargarRutas();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        //Primero obtenemos los valores desde los controles
        String nombre = this.txtNombre.getText();
        String via = this.txtVia.getText();
        String distrito = this.txtDistrito.getText();
        Provincia provincia = Provincia.valueOf(this.txtProvincia.getSelectedItem().toString());
        
        Ruta nuevaRuta = new Ruta(nombre,via,distrito,provincia);
        
        this.objGestionRutas.insertar(nuevaRuta);
        
        this.cargarRutas();
        
        JOptionPane.showMessageDialog(null, "Datos Guardados!");
    }//GEN-LAST:event_btnAgregarActionPerformed

