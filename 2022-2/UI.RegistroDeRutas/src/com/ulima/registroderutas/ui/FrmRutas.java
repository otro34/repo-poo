    private void setTableModel() {
        rutasDtModel.addColumn("ID");
        rutasDtModel.addColumn("Nombre");
        rutasDtModel.addColumn("Via");
        rutasDtModel.addColumn("Distrito");
        rutasDtModel.addColumn("Provincia");
        this.grdRutas.setModel(rutasDtModel);
    }
    
    private void cargarRutas(){
        ArrayList<Ruta> rutas = this.objGestionRutas.getRutas();
        
        this.cargarRutasEnTabla(rutas);
    }
    
    private void filtrarRutas(String nombre) {
        ArrayList<Ruta> rutas = this.objGestionRutas.filtrar(nombre);
        
        this.cargarRutasEnTabla(rutas);
    }
    
    private void cargarRutasEnTabla(ArrayList<Ruta> rutas) {
        //Método para cargar las rutas enb la tabla
        this.rutasDtModel.setRowCount(0);
        
        for (Ruta ruta:rutas) {
            String[] fila = new String[5];
            
            fila[0] = Integer.toString(ruta.getId());
            fila[1] = ruta.getNombre();
            fila[2] = ruta.getVia();
            fila[3] = ruta.getDistrito();
            fila[4] = ruta.getProvincia().toString();
            
            this.rutasDtModel.addRow(fila);
        }
    }

