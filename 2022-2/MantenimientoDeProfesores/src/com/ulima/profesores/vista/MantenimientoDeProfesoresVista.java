    private void cargarProfesoresDesdeLista(){
        
        this.profesorTableModel.setRowCount(0);
        
        ArrayList<Profesor> profesores = this.objGestionProfesor.getProfesores();
        
        for(Profesor profesor: profesores) {
            String[] fila = new String[5];
            
            fila[0] = Integer.toString(profesor.getId());
            fila[1] = profesor.getNombre();
            fila[2] = profesor.getApellido();
            fila[3] = profesor.getCategoria().toString();
            fila[4] = profesor.getGrado().toString();
            
            this.profesorTableModel.addRow(fila);
        }
        
        
    }

