    private void cargarVideojuegos() {
        this.tableModel.setRowCount(0);
        
        ArrayList<Videojuego> videojuegos = this.gestionVideojuegos.getVideojuegos();
        
        for (Videojuego videojuego: videojuegos) {
            String[] fila = new String[5];
            
            fila[0] = Integer.toString(videojuego.getId());
            fila[1] = videojuego.getTitulo();
            fila[2] = videojuego.getPublicador();
            fila[3] = videojuego.getFechaLanzamiento();
            fila[4] = videojuego.getGenero().toString();
            
            this.tableModel.addRow(fila);
        }
    }

