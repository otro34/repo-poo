    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        int id = this.txtId.getText().equals("") ? 0 : Integer.parseInt(this.txtId.getText());
        String titulo = this.txtTitulo.getText();
        String publicador = this.txtPublicador.getText();
        String fecha = this.txtFecha.getText();
        Genero genero = Genero.valueOf(this.cmbGenero.getSelectedItem().toString());
        
        Videojuego juego = new Videojuego(titulo, publicador, fecha, genero);
        
        this.gestionVideojuegos.agregar(juego);
        
        this.cargarVideojuegos();
        
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int selectedRow = this.tblVideojuegos.getSelectedRow();
        
        if (selectedRow >= 0) {
            int codigoVideojuego = Integer.parseInt(this.tblVideojuegos.getValueAt(selectedRow, 0).toString());
            Videojuego videojuego = this.gestionVideojuegos.buscar(codigoVideojuego);
            
            if (videojuego != null) {
                this.gestionVideojuegos.eliminar(videojuego);
                this.cargarVideojuegos();
            }
                              
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

