    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        
        int id = this.txtId.getText().equals("") ? 0 : Integer.parseInt(this.txtId.getText());
        String nombre = this.txtNombre.getText();
        String apellido = this.txtApellido.getText();
        
        Categoria categoria = Categoria.valueOf(this.cmbCategoria.getSelectedItem().toString());
        Grado grado = Grado.valueOf(this.cmbGrado.getSelectedItem().toString());
        
        Profesor profesor = new Profesor(id,nombre,apellido,categoria,grado);
        
        if (this.txtId.getText().equals("")) {
            objGestionProfesor.insertar(profesor);
        } else {
            objGestionProfesor.actualizar(profesor);
        }
        
        JOptionPane.showMessageDialog(this, "Profesor guardado");
        
        this.limpiarCampos();
        
        this.cargarProfesores();
    }//GEN-LAST:event_btnGuardarActionPerformed

