    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here: 
        int selectedIndex = this.tblProfesores.getSelectedRow();
        
        if ( selectedIndex >=0 ) {
            int codigoProfesor = Integer.parseInt(this.profesorTableModel.getValueAt(selectedIndex, 0).toString());
            
            Profesor profesor = this.objGestionProfesor.buscar(codigoProfesor);
            
            if (profesor != null) {
                this.txtId.setText(String.valueOf(profesor.getId()));
                this.txtApellido.setText(profesor.getApellido());
                this.txtNombre.setText(profesor.getNombre());
                this.cmbCategoria.setSelectedItem(profesor.getCategoria().toString());
                this.cmbGrado.setSelectedItem(profesor.getGrado().toString());
            }
        }
               
            
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = this.tblProfesores.getSelectedRow();
        
        if (selectedRow >= 0){
            int codigoProfesor = Integer.parseInt(this.profesorTableModel.getValueAt(selectedRow, 0).toString());
            Profesor profesor = this.objGestionProfesor.buscar(codigoProfesor);
            
            if (profesor != null) {
                this.objGestionProfesor.remover(profesor);
                this.cargarProfesores();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

