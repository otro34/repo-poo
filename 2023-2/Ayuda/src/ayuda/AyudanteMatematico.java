public class AyudanteMatematico {
      
    public static int Suma(int a, int b) {
        return a + b;
    }
    
    public static int Resta(int a, int b) {
        return a - b;
    }
    
    public static int Multiplicacion(int a, int b) {
        return a * b;
    }
    
    public static int Divison(int a, int b) {
         return a / b;
         
    }
    
    
    
    
    
}
