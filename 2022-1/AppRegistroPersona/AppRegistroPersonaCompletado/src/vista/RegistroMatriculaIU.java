    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        // Registra a un curso en el arreglo de objetos Curso
        // Recuperar los datos de un alumno
        int nroMatricula = Integer.valueOf(this.txtNroMatricula.getText());
        Alumno oAlumno = this.modeloListaAlumno.
                get(this.lstAlumnos.getSelectedIndex());
        Curso oCurso = this.modeloComboCurso.
                getElementAt(this.cboCursos.getSelectedIndex());
              
        Matricula oMatricula = new Matricula(nroMatricula,fechaActual,oAlumno,oCurso);
        boolean resultado = this.registroMatricula.agregar(oMatricula);
        if (resultado==true){
            JOptionPane.showMessageDialog(null, "Grabado correctamente!!");
        }else{
            JOptionPane.showMessageDialog(null, "Problema al guardar los datos!!");
        }    
        this.txtNroMatricula.setText(null);
        this.txtFechaMatricula.setText(null);
        
        this.cboCursos.setSelectedIndex(0);
    }//GEN-LAST:event_btnGrabarActionPerformed

