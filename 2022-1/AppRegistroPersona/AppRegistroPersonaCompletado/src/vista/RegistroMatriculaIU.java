    private void llenarCursosEnComboBox(){
        Curso[] listaCursos = registroCurso.getLista();
        for (int i = 0; i < listaCursos.length; i++) {
            Curso oCurso = (Curso)listaCursos[i];
            this.modeloComboCurso.addElement(oCurso);
        }
    }
    
    private void llenarAlumnosEnLista(){
        Persona[] listaAlumnos = registroPersona.getListaAlumnos();
        for (int i = 0; i < listaAlumnos.length; i++) {
            Alumno oAlumno = (Alumno)listaAlumnos[i];
            this.modeloListaAlumno.addElement(oAlumno);
        }
    }
    
