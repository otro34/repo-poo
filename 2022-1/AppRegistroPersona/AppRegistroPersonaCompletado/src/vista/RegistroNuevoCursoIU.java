    private void llenarDocentesEnComboBox(){
        Persona[] listaDocentes = registroPersona.getListaDocentes();
        for (int i = 0; i < listaDocentes.length; i++) {
            Docente oDocente = (Docente)listaDocentes[i];
            this.modeloComboDocente.addElement(oDocente);
        }
    }
    
