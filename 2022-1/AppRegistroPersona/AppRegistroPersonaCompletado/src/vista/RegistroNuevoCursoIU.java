    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        // Registra a un curso en el arreglo de objetos Curso
        // Recuperar los datos de un alumno
        int codCurso = Integer.valueOf(this.txtCodCurso.getText());
        String nombre = this.txtNombreCurso.getText();
        int nroHoras = Integer.valueOf(this.txtNroHoras.getText());
        // Recuperar el docente seleccionado de la lista desplegable (ComboBox)
        Docente oDocente = (Docente)this.cboDocente.getSelectedItem();
        
        Curso oCurso = new Curso(codCurso,nombre,nroHoras,oDocente);
        boolean resultado = this.registroCurso.agregar(oCurso);
        if (resultado==true){
            JOptionPane.showMessageDialog(null, "Grabado correctamente!!");
        }else{
            JOptionPane.showMessageDialog(null, "Problema al guardar los datos!!");
        }    
        this.txtCodCurso.setText(null);
        this.txtNombreCurso.setText(null);
        this.txtNroHoras.setText(null);
        this.cboDocente.setSelectedIndex(0);
    }//GEN-LAST:event_btnGrabarActionPerformed

